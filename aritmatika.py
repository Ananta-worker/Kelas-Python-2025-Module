# Module aritmetika


def penjumlahan(a, b):
    print(f'Hasil Penjumlahan {a}+{b} = {a+b}')


def pengurangan(a, b):
    print(f'Hasil Pengurangan {a}-{b} = {a - b}')


def perkalian(a, b):
    print(f"Hasil Perkalian {a} x {b} = {a * b}")


def pembagian(a, b):
    print(f'Hasil Pembagian {a}:{b} = {a / b}')
